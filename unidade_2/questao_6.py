"""Crie uma variável chamada celsius e atribua a ela um valor em graus Celsius (por exemplo, 25). Em seguida,
converta essa temperatura para Fahrenheit usando a fórmula correspondente que você pode encontrar em uma busca
simples na internet. Armazene o resultado em uma variável chamada fahrenheit e exiba a seguinte mensagem com
print: "A temperatura de [celsius]°C em Fahrenheit é [fahrenheit]°F"""

